DANCE_NAMES = {i + 1: dance for i, dance in enumerate(DANCES)}

PERSIAN_TO_ENGLISH = {
    "۰": "0", "۱": "1", "۲": "2", "۳": "3", "۴": "4",
    "۵": "5", "۶": "6", "۷": "7", "۸": "8", "۹": "9",
}


def persian_to_int(text: str) -> int | None:
    converted = "".join(PERSIAN_TO_ENGLISH.get(c, c) for c in text.strip())
    try:
        return int(converted)
    except ValueError:
        return None


def get_dance(number: int) -> str | None:
    return DANCE_NAMES.get(number)


def total_dances() -> int:
    return len(DANCES)
